// a is the initial index for the piece that we want to move
// b is the target index

public class twoIntegers {
	private int a, b;

	public twoIntegers() {
		a = 0;
		b = 0;
	}

	public twoIntegers(int a, int b) {
		this.a = a;
		this.b = b;
	}
	
	public void setA(int a) {
		this.a = a;
	}
	
	public void setB(int b) {
		this.b = b;
	}
	
	public int getA() {
		return a;
	}
	
	public int getB() {
		return b;
	}

}
